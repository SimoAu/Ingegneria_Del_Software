package progettoIngegneriaArtifactID;

public class data {
	
	int giorno;
	int mese;
	int anno;

	public data(int giorno, int mese, int anno) {
	
		this.giorno = giorno;
		this.mese = mese;
		this.anno = anno;
		
	}

	public int getGiorno() {
		return giorno;
	}

	public int getMese() {
		return mese;
	}


	public int getAnno() {
		return anno;
	}

}
